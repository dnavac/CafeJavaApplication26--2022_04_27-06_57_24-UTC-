    double calculo_pago(int n, String chose){
        double precio=0,pago_total;
        switch (chose) {
            case "Capuchino":
                precio=1;
                break;
            case "Latte":
                precio=1.5;
                break;
            case "Moca":
                precio=2;
                break;
            case "Coke":
                precio=2;
                break;
            case "Fanta":
                precio=1;
                break;
            case "Sprite":
                precio=1.5;
                break;
            case "Cake":
                precio=3;
                break;
            case "Pie":
                precio=5;
                break;
            case "Brownies":
                precio=4;
                break;
            default:
                break;
        }
        pago_total=n*precio;
        return pago_total;
        
      
    }
