    private void jcombocafeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcombocafeActionPerformed

        int posicion;
        posicion = jcombocafe.getSelectedIndex();

        switch (posicion) {
            case 0:
                jCombotipos.removeAllItems();
                jCombotipos.addItem("Capuchino");
                jCombotipos.addItem("Latte");
                jCombotipos.addItem("Moca");

                break;
            case 1:

                jCombotipos.removeAllItems();
                jCombotipos.addItem("Coke");
                jCombotipos.addItem("Fanta");
                jCombotipos.addItem("Sprite");

                break;
            case 2:
                jCombotipos.removeAllItems();
                jCombotipos.addItem("Cake");
                jCombotipos.addItem("Pie");
                jCombotipos.addItem("Brownies");

                break;
        }

    }//GEN-LAST:event_jcombocafeActionPerformed

    private void purchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseActionPerformed
        
        int n;
        String chose;
        double pago_total;
        double billete;
        double cambio1;
                
        
        n=Integer.parseInt(cantidad1.getText());
        chose=jCombotipos.getSelectedItem().toString();
        
        pago_total=calculo_pago(n,chose);
       
        DecimalFormat df=new DecimalFormat("##0.0");
        
       totalprice.setText(df.format(pago_total));
        billete= Double.parseDouble(recibido.getText());
        
        cambio1=billete-pago_total;
        DecimalFormat dg =new DecimalFormat("##0.0");
        cambio.setText(dg.format(cambio1));
    }//GEN-LAST:event_purchaseActionPerformed

